console.log (`Keliling ${keliling}`)




// Rumus Persegi Panjang

var p = 10
var l = 5

var luas = p * l
var keliling = 2 * (p + l)

console.log (`Luas ${luas}`)
console.log (`Keliling ${keliling}`)




// Rumus segitiga

var a = 10
var t = 5
var s = 5

luas = a * t / 2
keliling = a + t + s

