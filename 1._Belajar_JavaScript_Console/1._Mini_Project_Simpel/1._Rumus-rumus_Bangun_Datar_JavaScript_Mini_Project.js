console.log (`Keliling ${keliling}`)




// Rumus lingkaran

var phi = 3.14
var r = 5


luas = phi * r ** 2
keliling = 2 * phi * r

console.log (`Luas ${luas}`)
console.log (`Keliling ${keliling}`)




