// For Nested

for (a = 0; a < 3; a++) {
    for (b = 0; b < 3; b++) {
        console.log ("a", a, "b", b)
    }
}


for (d = 0; d < 2; d++) {
    for (c = 0; c < 3; c++) {
        console.log ("d", d, "c", c)
    }
}


