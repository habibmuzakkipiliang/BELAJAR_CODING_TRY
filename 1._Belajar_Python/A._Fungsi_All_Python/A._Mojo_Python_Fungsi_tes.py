def switch ():
    
    # Switch Case 
    
    
    print ("\n Switch Case \n")
    
    
    level = 3
    
    match (level):
        
        case 1:
            print ("1")
            
        case 2:
            print ("2")
            
        case 3:
            print ("3")
            
        case 4:
            print ("4")
            
        case 5:
            print ("5")
            
        case _:
            print ("Semula")
            
            
    print ("\n --- Batas --- \n")
    
    
    
    
    cek = 1
    
    match (cek):
        
        case 1:
            print ("Iya")
            
        case 2:
            print ("Gak")
            
        case _:
            print ("Semula")
            
            
    print ("\n --- Batas --- \n")
    
switch ()




