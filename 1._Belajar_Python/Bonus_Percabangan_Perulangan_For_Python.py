# For Nested 

for a in range (3):
    for b in range (2):
        print ("a", a, "b", b)
        
        
for c in range (3):
    for d in range (2):
        print ("c", c, "d", d)
        
        
for h in range (2):
    for g in range (3):
        print ("h", h, "g", g)
        
        
for k in range (2):
    for s in range (2):
        print ("k", k, "s", s)
        
        
for m in range (3):
    for l in range (3):
        print ("m", m, "l", l)
        
        
print ("\n --- Batas --- \n")
        
        
        
        
