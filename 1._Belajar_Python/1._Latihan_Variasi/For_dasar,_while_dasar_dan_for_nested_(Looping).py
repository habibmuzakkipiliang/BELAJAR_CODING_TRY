# For dasar, while dasar dan for nested (Looping)


print ("\n  For dasar, while dasar dan for nested (Looping) \n")


print ("\n For dasar \n")


for a in range (5):
    print ("A =", a)
    

print ("\n --- Batas --- \n")




for b in range (5):
    print ("B =", b)
    
    
print ("\n --- Batas --- \n")




for c in range (6):
    print ("C =", c)
    
    
print ("\n --- Batas --- \n")




for d in range (6):
    print ("D =", d)
    
    
print ("\n --- Batas --- \n")




for e in range (6):
    print ("E =", e)
    
    
print ("\n --- Batas --- \n")




print ("\n While dasar \n")


a = 5

while a < 10:
    print ("A =", a)
    a = a + 1
    
    
print ("\n --- Batas --- \n")




b = 5

while b < 15:
    print ("B =", b)
    b = b + 1
    
    
print ("\n --- Batas --- \n")




c = 5

while c < 5:
    print ("C =", c)
    c = c + 1
    
    
print ("\n --- Batas --- \n")




d = 5

while d < 10:
    print ("D =", d)
    d = d + 1
    
    
print ("\n --- Batas --- \n")




e = 5

while e < 10:
    print ("E =", e)
    e = e + 1
    
    
print ("\n --- Batas --- \n")




print ("\n For Nested \n")


for a in range (5):
    for b in range (6):
        print ("A =", a, "B =", b)
        
        
print ("\n --- Batas --- \n")




for c in range (7):
    for d in range (6):
        print ("C =", c, "D =", d)
        
        
print ("\n --- Batas --- \n")




for e in range (6):
    for g in range (7):
        print ("E =", e, "G =", g)
        
        
print ("\n --- Batas --- \n")




for x in range (7):
    for y in range (8):
        print ("X =", x, "Y =", y)
        
        
