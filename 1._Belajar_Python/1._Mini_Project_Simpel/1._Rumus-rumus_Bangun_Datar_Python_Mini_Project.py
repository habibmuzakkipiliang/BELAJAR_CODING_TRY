# Rumus-rumus Bangun datar Coding Python

# Rumus Persegi 

s = 5

luas = s * s
keliling = 4 * s

print (f"Luas {luas}")
print (f"Keliling {keliling}") 




# Rumus Persegi panjang

p = 10
l = 5

luas = p * l
keliling = 2 + (p * l)

print (f"Luas {luas}")
print (f"Keliling {keliling}")




# Rumus Segitiga

a = 10
t = 5
s = 5

luas = a * t / 2
keliling = a + t + s

print (f"Luas {luas}")
print (f"Keliling {keliling}")




# Rumus Lingkaran

phi = 3.14
r = 5

luas = phi * r ** 2
keliling = 2 * phi * r


print (f"Luas {luas}")
print (f"Keliling {keliling}")




# Rumus Belah Ketupat

d1 = 5
d2 = 5
s = 10

luas = d1 * d2 / 2
keliling = 4 * s

print (f"Luas {luas}")
print (f"Keliling {keliling}")




# Rumus Layang-Layang

